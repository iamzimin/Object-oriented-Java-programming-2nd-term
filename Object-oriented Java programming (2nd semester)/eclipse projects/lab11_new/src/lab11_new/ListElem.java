package lab11_new;

public class ListElem {
	
	ListElem next;
	ListElem prev;
	String value;
	
	public String getData() {
		return value;
	}
	public void setData(String str) {
		this.value = str;
	}
	
	public ListElem getNext() {
		return next;
	}
	public ListElem getPrev() {
		return prev;
	}
	
	public void setNext(ListElem next) {
		this.next = next;
	}
	public void setPrev(ListElem prev) {
		this.prev = prev;
	}
	
}